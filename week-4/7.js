// String
let name = "Alice";
console.log("String:", name);

// Number
let age = 25;
console.log("Number:", age);

// Boolean
let isStudent = true;
console.log("Boolean:", isStudent);

// Null
let emptyValue = null;
console.log("Null:", emptyValue);

// Undefined
let undefinedValue;
console.log("Undefined:", undefinedValue);

// Object
let person = {
    firstName: "Alice",
    lastName: "Johnson",
    age: 25
};
console.log("Object:", person);

// Array
let colors = ["red", "green", "blue"];
console.log("Array:", colors);

// Function
function greet() {
    return "Hello, World!";
}
console.log("Function:", greet);

// Symbol
let uniqueID = Symbol("id");
console.log("Symbol:", uniqueID);

// BigInt (useful for very large numbers)
let largeNumber = BigInt("1234567890123456789012345678901234567890");
console.log("BigInt:", largeNumber);
